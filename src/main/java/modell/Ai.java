    private int dbmg;
    private int def;
    private int doge;

    public Ai(){

    }

    public int getHp() {
        return hp;
    }

    public void setHp(int hp) {
        this.hp = hp;
    }

    public int getDbmg() {
        return dbmg;
    }

    public void setDbmg(int dbmg) {
        this.dbmg = dbmg;
    }

